    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            //Verificar se Username esta disponível

            if (username.getText().equals("") || password.getText().equals("")) {
                txtInfo.setText("Preencha ambos campos para se cadastrar");
                return;
            }

            boolean disponivel = stub.disponibilidade(username.getText());

            if (disponivel) {

                if (!stub.cadastrar(username.getText(), password.getText())) {
                    txtInfo.setText("Erro ao cadastrar usuário");
                } else {
                    txtInfo.setText("Usuário cadastrado com sucesso!");
                }

            } else {
                txtInfo.setText("Este nome de usuário ja esta em uso");
            }

        } catch (RemoteException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
            txtInfo.setText("Sem conexão com o servidor");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {

            // Verifica se o usuario esta online
            String id_user = stub.usuarioOnline(username.getText());

            if (!id_user.equals("")) {
                txtInfo.setText("Este usuário ja esta conectado");
                return;
            }

            // Realiza autenticação do usuario
            String id_login = stub.login(username.getText(), password.getText());
            if (!id_login.equals("")) {
                usuario_logado = new Usuario(username.getText(), id_login);
                txtInfo.setText("Conectado como: " + username.getText());
                btnLogout.setVisible(true);
                btnAtualizar.setVisible(true);
                btnChat.setVisible(true);
                btnLogin.setVisible(false);
                atualizaTabela();
            } else {
                txtInfo.setText("Não foi possivel realizar login com as credenciais");
            }
        } catch (RemoteException ex) {
            txtInfo.setText(ex.getMessage());
        }

    }//GEN-LAST:event_btnLoginActionPerformed


    private void btnChatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChatActionPerformed
        // Busca informações da tabela posicionada
        // e abre chat em ambos clientes
        ServicoChat stub = this.stub;
        int selectedRow = tabelaUsuarios.getSelectedRow();
        if (selectedRow >= 0) {
            String id = tabelaUsuarios.getValueAt(selectedRow, 0).toString();
            String nome = tabelaUsuarios.getValueAt(selectedRow, 1).toString();

            if (!id.equals(usuario_logado.getId())) {
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        new Chat(stub, usuario_logado.getId(), usuario_logado.getNome(), id, nome).setVisible(true);
                    }
                });
            } else {
                txtInfo.setText("Você nao pode iniciar uma conversa consigo mesmo");
            }

        } else {
            txtInfo.setText("Nenhum usuário selecionado");
        }


    }//GEN-LAST:event_btnChatActionPerformed

    class TimerTabela extends TimerTask {

        public void run() {

            if (usuario_logado != null) {
                try {
                    atualizaTabela();
                } catch (RemoteException ex) {
                    Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }
    }

    private void atualizaTabela() throws RemoteException {
        // Busca do servidor os usuarios conectados
        ArrayList<Usuario> conectados = stub.buscaUsuarios();
        //model.getDataVector().removeAllElements();

        tabelaUsuarios.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "ID", "Nome", "Status"
                }
        ));

        DefaultTableModel model = (DefaultTableModel) tabelaUsuarios.getModel();

        for (int i = 0; i < conectados.size(); i++) {
            
            String status = "Online";
            
            if (conectados.get(i).getId().equals(usuario_logado.getId())){
                status = "Você";
            }
                    
            if (stub.getMensagem(conectados.get(i).getId(), usuario_logado.getId()) != null){
                status = "Novas Mensagens!";
            }
            
            model.addRow(new Object[]{conectados.get(i).getId(), conectados.get(i).getNome(), status});
        }
    }
    

